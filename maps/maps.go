// Package maps contains useful functions to work with maps.
package maps

// Split takes a map and returns its keys and values as two separate slices.
func Split[K comparable, V any](m map[K]V) ([]K, []V) {
	keys := make([]K, 0, len(m))
	values := make([]V, 0, len(m))
	for k, v := range m {
		keys = append(keys, k)
		values = append(values, v)
	}
	return keys, values
}
